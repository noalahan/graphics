function drawMap() {
  for (x = 0; x < 32; x++) {
    for (y = 0; y < 32; y++) {
      let a = g_map[x][y];
      if (a > 0) {
        var body = new Cube();
        // body.color = [0.8, 1, 1, 1];
        body.matrix.translate(0, -0.75, 0);
        body.matrix.scale(0.5, 0.5 * a, 0.5);
        body.matrix.translate(x - 16, 0, y - 16);
        body.render();
      }
    }
  }
}

